package com.wikidreams.opencv.advertisement;

/**
 * @author WIT Software - Daniel Vieira
 * @version 1.0 - 2016
 */

public class Advertisement {

	private int start;
	private int end;

	public Advertisement(int start, int end) {
		super();
		this.start = start;
		this.end = end;
	}

	public int getStart() {
		return start;
	}

	public int getEnd() {
		return end;
	}

}
